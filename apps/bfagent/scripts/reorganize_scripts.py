def move_file(src: Path, dst: Path, category: str):
    """Move file with safety checks"""
    if not src.exists():
        print(f"⚠️  {category}: {src.name} not found, skipping")
        return False
    
    if dst.exists():
        print(f"⏭️  {category}: {dst.name} already exists, skipping")
        return False
    
    try:
        shutil.copy2(src, dst)
        print(f"✅ {category}: {src.name} → {dst.name}")
        return True
    except Exception as e:
        print(f"❌ {category}: Error moving {src.name}: {e}")
        return False


def main():
    print("🔄 Script Reorganization Tool")
    print("=" * 60)
    
    moved_count = 0
    
    # Move production tools to tools/
    print("\n📦 Moving Production Tools to tools/...")
    for script in PRODUCTION_TOOLS:
        src = SCRIPTS_DIR / script
        dst = TOOLS_DIR / script
        if move_file(src, dst, "Tools"):
            moved_count += 1
    
    # Move helper scripts to helpers/
    print("\n🔧 Moving Helper Scripts to helpers/...")
    for script in HELPER_SCRIPTS + CLEANUP_SCRIPTS:
        src = SCRIPTS_DIR / script
        dst = HELPERS_DIR / script
        if move_file(src, dst, "Helpers"):
            moved_count += 1
    
    # Move management commands to helpers/
    print("\n📋 Moving Management Commands to helpers/...")
    for script in MGMT_COMMANDS_TO_MOVE:
        src = MGMT_COMMANDS / script
        dst = HELPERS_DIR / script
        if move_file(src, dst, "Commands"):
            moved_count += 1
    
    # Move obsolete scripts to archive/
    print("\n🗄️  Moving Obsolete Scripts to archive/...")
    for script in ARCHIVE_SCRIPTS:
        src = SCRIPTS_DIR / script
        dst = ARCHIVE_DIR / script
        if move_file(src, dst, "Archive"):
            moved_count += 1
    
    print("\n" + "=" * 60)
    print(f"✅ Reorganization complete! {moved_count} files moved.")
    print("\n📚 Next steps:")
    print("1. Review moved files and verify correctness")
    print("2. Update Makefile paths if necessary")
    print("3. Update imports in other scripts")
    print("4. Run tests to ensure nothing broke")
