def dry_run():
    """Show what would be moved without actually moving"""
    print("🔄 Script Reorganization - DRY RUN")
    print("=" * 60)
    
    # Collect all categorized scripts
    all_categorized = set(
        PRODUCTION_TOOLS + 
        HELPER_SCRIPTS + 
        CLEANUP_SCRIPTS + 
        ARCHIVE_SCRIPTS +
        MGMT_COMMANDS_TO_MOVE
    )
    
    # Scan actual scripts directory
    all_scripts = [f.name for f in SCRIPTS_DIR.glob("*.py") if f.is_file()]
    uncategorized = [s for s in all_scripts if s not in all_categorized]
    
    # Count statistics
    tools_count = len([s for s in PRODUCTION_TOOLS if (SCRIPTS_DIR / s).exists()])
    helpers_count = len([s for s in HELPER_SCRIPTS + CLEANUP_SCRIPTS if (SCRIPTS_DIR / s).exists()])
    archive_count = len([s for s in ARCHIVE_SCRIPTS if (SCRIPTS_DIR / s).exists()])
    mgmt_count = len([s for s in MGMT_COMMANDS_TO_MOVE if (MGMT_COMMANDS / s).exists()])
    
    print(f"\n📊 Summary:")
    print(f"  Total scripts in scripts/: {len(all_scripts)}")
    print(f"  → tools/: {tools_count}")
    print(f"  → helpers/: {helpers_count + mgmt_count}")
    print(f"  → archive/: {archive_count}")
    print(f"  ⚠️  Uncategorized: {len(uncategorized)}")
    
    print("\n📦 Production Tools → tools/ ({})".format(tools_count))
    for script in sorted(PRODUCTION_TOOLS):
        src = SCRIPTS_DIR / script
        if src.exists():
            print(f"  ✓ {script}")
        else:
            print(f"  ⚠️  {script} (not found)")
    
    print("\n🔧 Helper Scripts → helpers/ ({})".format(helpers_count + mgmt_count))
    for script in sorted(HELPER_SCRIPTS + CLEANUP_SCRIPTS):
        src = SCRIPTS_DIR / script
        if src.exists():
            print(f"  ✓ {script}")
        else:
            print(f"  ⚠️  {script} (not found)")
    
    print("\n📋 Management Commands → helpers/")
    for script in sorted(MGMT_COMMANDS_TO_MOVE):
        src = MGMT_COMMANDS / script
        if src.exists():
            print(f"  ✓ {script}")
        else:
            print(f"  ⚠️  {script} (not found)")
    
    print("\n🗄️  Obsolete Scripts → archive/ ({})".format(archive_count))
    for script in sorted(ARCHIVE_SCRIPTS):
        src = SCRIPTS_DIR / script
        if src.exists():
            print(f"  ✓ {script}")
        else:
            print(f"  ⚠️  {script} (not found)")
    
    if uncategorized:
        print(f"\n⚠️  UNCATEGORIZED SCRIPTS ({len(uncategorized)}):")
        print("    These scripts are NOT in any category and will NOT be moved:")
        for script in sorted(uncategorized):
            # Auto-suggest category based on name patterns
            suggestion = ""
            if any(x in script for x in ["cleanup", "clean_", "fix_", "auto_fix"]):
                suggestion = " → helpers/"
            elif any(x in script for x in ["init_", "setup_", "create_", "reset_"]):
                suggestion = " → helpers/"
            elif any(x in script for x in ["check_", "validate_"]):
                suggestion = " → archive/ or helpers/"
            elif "reorganize" in script:
                suggestion = " → helpers/ (this script)"
            else:
                suggestion = " → tools/ or archive/?"
            
            print(f"  ❓ {script}{suggestion}")
    
    print("\n" + "=" * 60)
    print("💡 To execute: python scripts/reorganize_scripts.py --execute")
    print("💡 Review uncategorized scripts and add them to appropriate lists")


