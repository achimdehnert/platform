def _escape_po(text: str) -> str:
    """Escape a string for PO file format."""
    return (
        text
        .replace("\\", "\\\\")
        .replace('"', '\\"')
        .replace("\n", "\\n")
        .replace("\r", "\\r")
        .replace("\t", "\\t")
    )


def _make_po_entry(msgid: str, msgstr: str, comment: str = "") -> str:
    """Format a single PO entry."""
    lines = []
    if comment:
        lines.append(f"#. {comment}")
    lines.append(f'msgid "{_escape_po(msgid)}"')
    lines.append(f'msgstr "{_escape_po(msgstr)}"')
    lines.append("")
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Pipeline
# ---------------------------------------------------------------------------

class VoicePipeline:
    """Transforms HubVoiceDNA → Django .po files per language."""

    LANGUAGES = ["de", "en"]

    def __init__(self, output_root: Path) -> None:
        """
        output_root: the hub's project root where locale/ will be created.
        E.g. output_root = Path("generated_po/bieterpilot")
        """
        self.output_root = output_root

    def generate(self, dna: HubVoiceDNA) -> dict[str, Path]:
        """Generate .po files for all languages. Returns {lang: path}."""
        generated_at = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M +0000")
        outputs: dict[str, Path] = {}

        for lang in self.LANGUAGES:
            copy: LocalizedMicroCopy = getattr(dna.micro_copy, lang)
            copy_dict = copy.model_dump()

            # Build PO content
            po_content = PO_HEADER.format(
                hub=dna.hub,
                lang=lang,
                voice_description=dna.voice_description[:80],
                generated_at=generated_at,
                plural_forms=PLURAL_FORMS.get(lang, PLURAL_FORMS["en"]),
            )

            # Voice metadata as comments
            po_content += (
                f"# Voice: {', '.join(t.value for t in dna.tone)}\n"
                f"# Formal address (DE): {dna.use_formal_address}\n"
                f"# Banned words ({lang}): "
                f"{', '.join(getattr(dna, f'banned_words_{lang}', [])[:5])}\n\n"
            )

            # Entries grouped by category
            groups: dict[str, list[tuple[str, str, str]]] = {}
            for field_name, msgid in MSGID_MAP.items():
                category = msgid.split(".")[0]
                msgstr = str(copy_dict.get(field_name, ""))
                groups.setdefault(category, []).append((msgid, msgstr, field_name))

            category_comments = {
                "cta":     "Call-to-Action buttons",
                "status":  "Status indicators",
                "toast":   "Toast / notification messages",
                "error":   "Error messages",
                "label":   "Form labels and hints",
                "hint":    "Input placeholders and hints",
                "nav":     "Navigation items",
                "dialog":  "Confirmation dialogs",
            }

            for category, entries in groups.items():
                po_content += f"# ── {category_comments.get(category, category)} ──\n\n"
                for msgid, msgstr, field_name in entries:
                    po_content += _make_po_entry(msgid, msgstr, f"field: {field_name}")

            # Write .po file
            out_dir = self.output_root / dna.hub / "locale" / lang / "LC_MESSAGES"
            out_dir.mkdir(parents=True, exist_ok=True)
            out_path = out_dir / "django.po"
            out_path.write_text(po_content, encoding="utf-8")

            outputs[lang] = out_path
            print(f"  ✓ {dna.hub}/{lang}  → {out_path.relative_to(self.output_root.parent)}")

        return outputs

    def generate_all(self, dna_dir: Path) -> dict[str, dict[str, Path]]:
        """Generate .po files for all hub DNA YAMLs."""
        results: dict[str, dict[str, Path]] = {}
        for yaml_path in sorted(dna_dir.glob("*.yaml")):
            if yaml_path.name.startswith("_"):
                continue
            try:
                dna = HubVoiceDNA.from_yaml(str(yaml_path))
                results[dna.hub] = self.generate(dna)
            except Exception as e:
                print(f"  ✗ {yaml_path.stem}: {e}", file=sys.stderr)
        return results


