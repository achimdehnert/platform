# ---------------------------------------------------------------------------

PO_HEADER = """\
# Hub Language Identity System (ADR-052)
# Hub:       {hub}
# Language:  {lang}
# Voice:     {voice_description}
# Generated: {generated_at}
# DO NOT EDIT: Auto-generated by tools/voice_dna/pipeline.py
#              Edit hub_voice_dnas/{hub}.yaml then run: make generate-voice HUB={hub}
#
msgid ""
msgstr ""
"Project-Id-Version: {hub}\\n"
"Report-Msgid-Bugs-To: platform@iil.pet\\n"
"POT-Creation-Date: {generated_at}\\n"
"PO-Revision-Date: {generated_at}\\n"
"Last-Translator: voice-dna-pipeline\\n"
"Language-Team: IIL GmbH\\n"
"Language: {lang}\\n"
"MIME-Version: 1.0\\n"
"Content-Type: text/plain; charset=UTF-8\\n"
"Content-Transfer-Encoding: 8bit\\n"
"Plural-Forms: {plural_forms}\\n"

"""

