# ---------------------------------------------------------------------------
# Pipeline
# ---------------------------------------------------------------------------

class Pipeline:
    """Transforms HubDNA → pui-tokens-{hub}.css"""

    def __init__(self, output_dir: Path) -> None:
        self.output_dir = output_dir
        self.output_dir.mkdir(parents=True, exist_ok=True)

    def generate(self, dna: HubDNA) -> Path:
        """Generate CSS tokens file for a single hub."""
        from datetime import datetime, timezone

        grid_css = GRID_CSS.get(dna.layout.grid_variant.value, GRID_CSS["standard"])

        css = CSS_TEMPLATE.format(
            hub=dna.hub,
            display_name=dna.display_name,
            personality=dna.personality,
            aesthetic=dna.aesthetic,
            generated_at=datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC"),
            font_source=dna.typography.font_source,
            primary=dna.palette.primary,
            primary_hover=dna.palette.primary_hover,
            accent=dna.palette.accent,
            surface=dna.palette.surface,
            surface_alt=dna.palette.surface_alt,
            foreground=dna.palette.foreground,
            muted=dna.palette.muted,
            border=dna.palette.border,
            danger=dna.palette.danger,
            success=dna.palette.success,
            warning=dna.palette.warning,
            display_font=dna.typography.display_font,
            body_font=dna.typography.body_font,
            mono_font=dna.typography.mono_font,
            radius_sm=dna.layout.radius_sm,
            radius_md=dna.layout.radius_md,
            radius_lg=dna.layout.radius_lg,
            radius_xl=dna.layout.radius_xl,
            duration_fast=dna.motion.duration_fast,
            duration_normal=dna.motion.duration_normal,
            easing_standard=dna.motion.easing_standard,
            easing_spring=dna.motion.easing_spring,
            hover_lift=dna.motion.hover_lift,
            grid_variant=dna.layout.grid_variant.value,
            grid_css=grid_css,
            asymmetry_level=dna.layout.asymmetry_level.value,
            fingerprint_score=dna.fingerprint_score or "not-yet-audited",
        )

        out_path = self.output_dir / f"pui-tokens-{dna.hub}.css"
        out_path.write_text(css, encoding="utf-8")
        return out_path

    def generate_all(self, dna_dir: Path) -> list[Path]:
        """Generate CSS for all hub DNA YAMLs in a directory."""
        outputs = []
        for yaml_path in sorted(dna_dir.glob("*.yaml")):
            if yaml_path.name.startswith("_"):
                continue  # Skip schema/template files
            try:
                dna = HubDNA.from_yaml(str(yaml_path))
                out = self.generate(dna)
                print(f"  ✓ {dna.hub:20s} → {out.name}")
                outputs.append(out)
            except Exception as e:
                print(f"  ✗ {yaml_path.stem:20s} → ERROR: {e}", file=sys.stderr)
        return outputs
