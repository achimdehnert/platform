"""Extract Django model definitions via AST for reference-doc generation.

Produces Markdown tables with field names, types, constraints, and help_text.
No Django import required — pure AST analysis (works without running Django).
"""

from __future__ import annotations

import ast
import logging
from dataclasses import dataclass, field
from pathlib import Path

logger = logging.getLogger(__name__)


@dataclass
class FieldInfo:
    name: str
    field_type: str
    max_length: str = ""
    null: bool = False
    blank: bool = False
    unique: bool = False
    db_index: bool = False
    default: str = ""
    help_text: str = ""
    choices: str = ""
    related_model: str = ""


@dataclass
class ModelInfo:
    name: str
    docstring: str = ""
    fields: list[FieldInfo] = field(default_factory=list)
    meta_verbose_name: str = ""
    meta_ordering: str = ""
    parents: list[str] = field(default_factory=list)
    file_path: str = ""
    line_number: int = 0


def extract_models_from_file(file_path: Path) -> list[ModelInfo]:
    """Parse a single Python file and extract Django model definitions."""
    try:
        source = file_path.read_text(encoding="utf-8")
        tree = ast.parse(source, filename=str(file_path))
    except (SyntaxError, UnicodeDecodeError) as exc:
        logger.warning("Cannot parse %s: %s", file_path, exc)
        return []

    models: list[ModelInfo] = []
    for node in ast.iter_child_nodes(tree):
        if not isinstance(node, ast.ClassDef):
            continue

        # Check if it inherits from models.Model (or similar)
        parent_names = _get_base_names(node)
        if not _is_django_model(parent_names):
            continue

        model = ModelInfo(
            name=node.name,
            docstring=ast.get_docstring(node) or "",
            parents=parent_names,
            file_path=str(file_path),
            line_number=node.lineno,
        )

        for item in node.body:
            if isinstance(item, ast.Assign):
                _extract_field_from_assign(item, model)
            elif isinstance(item, ast.ClassDef) and item.name == "Meta":
                _extract_meta(item, model)

        models.append(model)

    return models


def extract_models_from_repo(
    repo_path: Path, *, apps_only: bool = True
) -> list[ModelInfo]:
    """Scan a repository for all Django models."""
    search_dir = repo_path / "apps" if apps_only and (repo_path / "apps").is_dir() else repo_path
    all_models: list[ModelInfo] = []

    for py_file in sorted(search_dir.rglob("models.py")):
        if "migrations" in py_file.parts or "test" in py_file.name:
            continue
        models = extract_models_from_file(py_file)
        all_models.extend(models)

    return all_models


def render_models_markdown(
    models: list[ModelInfo], *, repo_name: str = ""
) -> str:
    """Render extracted models as a Markdown reference document."""
    lines: list[str] = [
        "<!-- AUTO-GENERATED by /session-docu — DO NOT EDIT MANUALLY -->",
        f"<!-- Generated for: {repo_name} -->",
        "",
        "# Models Reference",
        "",
    ]

    if not models:
        lines.append("_No Django models found._")
        return "\n".join(lines)

    lines.append(f"**{len(models)} models** found.\n")

    for model in models:
        lines.append(f"## {model.name}")
        lines.append("")
        if model.docstring:
            first_line = model.docstring.strip().split("\n")[0]
            lines.append(f"> {first_line}")
            lines.append("")
        if model.parents:
            lines.append(f"**Inherits:** `{'`, `'.join(model.parents)}`")
            lines.append("")
        if model.meta_verbose_name:
            lines.append(f"**Verbose name:** {model.meta_verbose_name}")
        if model.meta_ordering:
            lines.append(f"**Ordering:** `{model.meta_ordering}`")
        lines.append("")

        if model.fields:
            lines.append("| Field | Type | Constraints | Description |")
            lines.append("|-------|------|-------------|-------------|")
            for f in model.fields:
                constraints = []
                if f.unique:
                    constraints.append("unique")
                if f.db_index:
                    constraints.append("indexed")
                if f.null:
                    constraints.append("nullable")
                if f.default:
                    constraints.append(f"default: `{f.default}`")
                if f.max_length:
                    constraints.append(f"max_length={f.max_length}")
                if f.choices:
                    constraints.append(f"choices: {f.choices}")
                constraint_str = ", ".join(constraints) if constraints else "—"
                help_str = f.help_text if f.help_text else "—"
                lines.append(
                    f"| `{f.name}` | `{f.field_type}` | {constraint_str} | {help_str} |"
                )
            lines.append("")
        else:
            lines.append("_No fields detected._\n")

    return "\n".join(lines)


