# Internal helpers
# ---------------------------------------------------------------------------


def _get_base_names(node: ast.ClassDef) -> list[str]:
    """Extract base class names as strings."""
    names = []
    for base in node.bases:
        if isinstance(base, ast.Attribute):
            names.append(f"{_attr_to_str(base)}")
        elif isinstance(base, ast.Name):
            names.append(base.id)
    return names


def _attr_to_str(node: ast.Attribute) -> str:
    if isinstance(node.value, ast.Name):
        return f"{node.value.id}.{node.attr}"
    if isinstance(node.value, ast.Attribute):
        return f"{_attr_to_str(node.value)}.{node.attr}"
    return node.attr


def _is_django_model(parent_names: list[str]) -> bool:
    model_bases = {"models.Model", "Model", "TenantAwareModel", "TimeStampedModel"}
    return any(p in model_bases or p.endswith(".Model") for p in parent_names)


def _extract_field_from_assign(node: ast.Assign, model: ModelInfo) -> None:
    """Extract field info from a class-level assignment like `name = models.CharField(...)`."""
    if len(node.targets) != 1 or not isinstance(node.targets[0], ast.Name):
        return
    if not isinstance(node.value, ast.Call):
        return

    field_name = node.targets[0].id
    if field_name.startswith("_"):
        return

    call = node.value
    field_type = _get_call_name(call)
    if not field_type or "Field" not in field_type:
        # Also catch models.TextField etc.
        if not (field_type and ("models." in field_type or field_type.endswith("Field"))):
            return

    info = FieldInfo(name=field_name, field_type=field_type)

    for kw in call.keywords:
        if kw.arg == "max_length":
            info.max_length = _get_literal_value(kw.value)
        elif kw.arg == "null":
            info.null = _get_literal_value(kw.value) == "True"
        elif kw.arg == "blank":
            info.blank = _get_literal_value(kw.value) == "True"
        elif kw.arg == "unique":
            info.unique = _get_literal_value(kw.value) == "True"
        elif kw.arg == "db_index":
            info.db_index = _get_literal_value(kw.value) == "True"
        elif kw.arg == "default":
            info.default = _get_literal_value(kw.value)
        elif kw.arg == "help_text":
            info.help_text = _get_literal_value(kw.value)
        elif kw.arg == "choices":
            info.choices = _get_literal_value(kw.value)
        elif kw.arg == "primary_key":
            if _get_literal_value(kw.value) == "True":
                info.unique = True

    # ForeignKey / OneToOneField first positional arg = related model
    if call.args and field_type in (
        "models.ForeignKey",
        "models.OneToOneField",
        "ForeignKey",
        "OneToOneField",
    ):
        info.related_model = _get_literal_value(call.args[0])

    model.fields.append(info)


def _extract_meta(node: ast.ClassDef, model: ModelInfo) -> None:
    for item in node.body:
        if isinstance(item, ast.Assign) and len(item.targets) == 1:
            name = getattr(item.targets[0], "id", "")
            if name == "verbose_name":
                model.meta_verbose_name = _get_literal_value(item.value)
            elif name == "ordering":
                model.meta_ordering = _get_literal_value(item.value)


def _get_call_name(call: ast.Call) -> str:
    if isinstance(call.func, ast.Attribute):
        return _attr_to_str(call.func)
    if isinstance(call.func, ast.Name):
        return call.func.id
    return ""


def _get_literal_value(node: ast.expr) -> str:
    """Best-effort extraction of literal values from AST nodes."""
    if isinstance(node, ast.Constant):
        return str(node.value)
    if isinstance(node, ast.Name):
        return node.id
    if isinstance(node, ast.Attribute):
        return _attr_to_str(node)
    if isinstance(node, ast.List):
        items = [_get_literal_value(elt) for elt in node.elts]
        return f"[{', '.join(items)}]"
    if isinstance(node, ast.Call):
        return _get_call_name(node) + "(...)"
    return "..."
