_SETTING_CATEGORIES = {
    "Database": {"DATABASE", "DB_", "CONN_"},
    "Cache & Redis": {"CACHE", "REDIS", "CELERY"},
    "Security": {"SECRET", "ALLOWED_HOSTS", "CSRF", "CORS", "AUTH", "PASSWORD", "SESSION"},
    "Email": {"EMAIL", "SMTP"},
    "Static & Media": {"STATIC", "MEDIA", "STORAGES"},
    "Logging": {"LOG"},
    "Internationalization": {"LANGUAGE", "TIME_ZONE", "USE_I18N", "USE_L10N", "USE_TZ", "LOCALE"},
}


def _categorize_settings(
    settings: list[SettingInfo],
) -> dict[str, list[SettingInfo]]:
    categories: dict[str, list[SettingInfo]] = {}
    for s in settings:
        cat = "General"
        for category, prefixes in _SETTING_CATEGORIES.items():
            if any(s.name.startswith(p) or p in s.name for p in prefixes):
                cat = category
                break
        categories.setdefault(cat, []).append(s)
    return categories


def _get_setting_value(node: ast.expr) -> str:
    """Best-effort value extraction."""
    if isinstance(node, ast.Constant):
        val = node.value
        if isinstance(val, str) and len(val) > 60:
            return val[:57] + "..."
        return str(val)
    if isinstance(node, ast.Name):
        return node.id
    if isinstance(node, ast.List):
        return f"[...{len(node.elts)} items]"
    if isinstance(node, ast.Dict):
        return f"{{...{len(node.keys)} keys}}"
    if isinstance(node, ast.Call):
        func = ""
        if isinstance(node.func, ast.Attribute):
            if isinstance(node.func.value, ast.Name):
                func = f"{node.func.value.id}.{node.func.attr}"
            else:
                func = node.func.attr
        elif isinstance(node.func, ast.Name):
            func = node.func.id
        return f"{func}(...)"
    if isinstance(node, ast.BoolOp):
        return "..."
    return "..."


def _sanitize_value(value: str) -> str:
    """Remove potential secrets from display."""
    secret_patterns = ["password", "secret", "token", "key", "api_key"]
    lower = value.lower()
    if any(p in lower for p in secret_patterns):
        return "***"
    if len(value) > 80:
        return value[:77] + "..."
    return value
